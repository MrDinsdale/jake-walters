export const indexToWord = (index) => {
    const numberWords = [
        'one',
        'two',
        'three',
        'four',
        'five',
        'six',
        'seven',
        'eight',
        'ten',
        'eleven',
        'twelve',
        'thirteen',
        'fourteen',
        'fifteen'
    ];

    return numberWords[index];
}

export default indexToWord;